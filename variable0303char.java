package JumpToJava.chap03;

public class variable0303char {
    public static void main(String[] args) {
        char a1 = 'a';//문자로 표현
        char a2 = 97;//아스키코드
        char a3 = '\u0061';//유니코드

        System.out.println(a1);
        System.out.println(a2);
        System.out.println(a3);

    }
}
